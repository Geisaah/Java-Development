package marathon.javadevelopment.introduction;

public class LoopEstructures1 {
    public static void main(String[] args) {
        //print all even number from 0 as 100000
        for (int i = 1; i <= 1000; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
